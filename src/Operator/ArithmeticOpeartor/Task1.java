package Operator.ArithmeticOpeartor;

public class Task1 {
    public static void main ( String[] args ) {
        int a = 10;
        int b = 20;

        // Normal Arithmetic Operator
        System.out.println (a+b);
        System.out.println (a-b);
        System.out.println (a*b);
        System.out.println (a/b);
        System.out.println (a%b);

        //Concatenation in java
        System.out.println (2+"3"+9+0);
        System.out.println (2+1+3+"2"+4+8);
        System.out.println (1.0+2.0+"4"+"4"+2+1);
        System.out.println ("1"+2+3+0+4+1+2);
        System.out.println (3+4+"Hello"+2+1+3);
    }
}
