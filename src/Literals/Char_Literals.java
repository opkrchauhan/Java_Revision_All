package Literals;

public class Char_Literals {
    public static void main ( String[] args ) {
        //Char Single quotes
        char ch ='a';
        System.out.println (ch);

        //Char literals as a number
        char ch1 = 062;
        System.out.println (ch1);

        // Char Unicode Representation
        char ch2 = '\u0061';
        System.out.println (ch2);

        //Backslash Literals
        System.out.println ("Hello I am khan Sir \nGood Morning ");
        System.out.println ("Hello I am khan Sir \tGood Morning ");
        System.out.println ("Hello I am khan Sir \bGood Morning ");
//        System.out.println ("Hello I am khan Sir \vGood Morning ");
        System.out.println ("Hello I am khan Sir \rGood Morning ");
        System.out.println ('I');
        System.out.println ("I am Op");

    }
}
