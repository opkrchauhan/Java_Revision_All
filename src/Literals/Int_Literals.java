package Literals;

public class Int_Literals {
    public static void main ( String[] args ) {
        //Decimal Literals
        int x = 1015;
        System.out.println (x);

        //Octal Literals
        int x1 = 0146;
        System.out.println (x1);

        // Hexa-Decimal Literals
        int x3 = 0x123456af;
        System.out.println (x3);

        //Binary Literals
        int binVal = 0b11010;
        System.out.println (binVal);


    }
}
